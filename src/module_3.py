        output.append(f"–ö—Ä–∞–∫–µ–Ω –Ω–µ –ø–æ–±–µ–∂–¥—ë–Ω, —Ç–∞–∫ –∫–∞–∫ –æ—Å—Ç–∞–ª–æ—Å—å {health} –∂–∏–∑–Ω–µ–π.")
